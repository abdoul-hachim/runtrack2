                            Le tanto est un couteau japonais de moins de 30 cm. 
                            Il était utilisé par des samouraïs en tant que lame 
                            perce-armure ou même par des femmes combattantes. 
                            Il en existe deux types principaux : l'hamidachi 
                            (avec une garde de petite dimension) et l'aikuchi 
                            (pas de garde, poignée en peau de poisson).
                        </p>
