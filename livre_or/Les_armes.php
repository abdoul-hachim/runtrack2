                        <p>
                            Le tachi est une autre sorte de sabre japonais utilisé principalement jusqu'au XIIIème siècle 
                            au Japon (période Koto). Il était l'arme de prédilection du samurai à cheval : suspendu à la ceinture de 
                            l'armure, il pendait sur le flanc de la monture, tranchant vers le bas. La longueur de la lame est d'environ 70 cm.
