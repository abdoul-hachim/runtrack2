                            forgé durant l'âge de bronze au Japon. Ce terme était utilisé pour désigner 
                            des épées courtes à lame droite de ce type. Il s'agit d'une forme d'épée très 
                            ancienne et peu utilisée dans les siècles qui suivirent.
                        </p>
