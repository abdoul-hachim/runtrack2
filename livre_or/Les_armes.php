                        <p>
                            Nodachi désigne littéralement "sabre de champ". Il s'agit d'un très 
                            long sabre dont la lame fait en moyenne entre 1m et 1m50 et qui était utilisé 
                            par les bushi sur les champs de bataille. Ils la portaient sur l'épaule, le plat de la lame 
                            sur l'épaule et le Tsuka en avant dans la main.
                        </p>
                        <p>
                            On l'utilisait principalement en extérieur du fait de sa grande taille rendant 
                            impraticable une utilisation en intérieur. Ainsi, il s'agissait d'une arme 
                            spécialement conçue pour défaire les cavaliers et leurs chevaux. La légende 
                            raconte même qu'en l'utilisant bien, on pouvait couper en deux un cheval et 
                            son cavalier d'un seul coup !
                        </p>
                        <p>
                            Cependant, cette arme ne fut que très peu utilisée, rapidement supplantée 
                            par le Naginata ou le Nagamaki, bien plus efficaces.
                        </p>
