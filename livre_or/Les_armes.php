                            Lorsqu'il fut invité chez le grand seigneur de guerre Oda Nobunaga 
                            (le "roi démon") Araki Murashige laissa son sabre à l'entrée 
                            comme de coutume. Il exécuta le salut traditionnel et Nobunaga 
                            comptait le décapiter avec les portes coulissantes se refermant 
                            sur sa nuque. Heureusement, Araki utilisa son Tessen pour bloquer 
