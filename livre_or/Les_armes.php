                            le Sasumata (double fourche), le Sodegarami (pointes) et le Tsukubō 
                            (en forme de T). Elles n'étaient pas conçues pour tuer, mais plutôt pour 
                            attraper. On les utilisa notamment pour s'occuper des prisonniers.
                        </p>
