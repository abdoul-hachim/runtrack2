            <article class="present-text">
                <h1>Le Samurai</h1>
                <p>
                    être un samouraï signifiait vivre selon un code moral strict, 
                    servir avec dévouement et loyauté, et aspirer à l'excellence 
                    dans tous les aspects de la vie. C'était une existence imprégnée de discipline, 
                    d'honneur et de courage, où le sens de la mission et du devoir transcendait les intérêts personnels
                </p>
                <p>
                    Le bushido, littéralement "la voie du guerrier", 
                    était le code moral et éthique auquel les samouraïs adhéraient. 
                    C'était bien plus qu'un simple ensemble de règles; c'était un code de vie qui guidait 
                    chaque aspect de la vie d'un samouraï, dictant son comportement sur le champ de bataille 
                    aussi bien que dans la vie quotidienne.
                </p>
                <div>
                    <h3>Voici quelques principes fondamentaux du bushido</h3>
                    <p>
                        Rectitude (Gi) : 
                        Les samouraïs étaient tenus de suivre la voie de la droiture et de la justice. 
                        Ils devaient agir de manière morale et éthique, en respectant les normes sociales 
                        et en faisant preuve d'intégrité dans leurs actions.
                    </p>
                    <p>
                        Courage (Yū) : 
                        Le courage était une qualité essentielle pour un samouraï. 
                        Ils étaient encouragés à affronter la peur et à agir avec bravoure, 
                        que ce soit sur le champ de bataille ou dans leur vie quotidienne.
                    </p>
                    <p>
                        Bonté (Jin) : 
                        Les samouraïs étaient tenus d'être bienveillants envers les autres, 
                        en particulier envers les plus faibles et les moins fortunés. Ils devaient 
                        faire preuve d'empathie, de compassion et de générosité envers ceux qui étaient 
                        dans le besoin.
                    </p>
                    <p>
                        Respect (Rei) : 
                        Le respect était fondamental dans la culture des samouraïs. 
                        Ils devaient respecter non seulement leurs supérieurs et leurs pairs, 
                        mais aussi leurs ennemis vaincus. Le respect pour la nature et les traditions 
                        était également important.
                    </p>
                    <p>
                        Honnêteté (Makoto) : 
                        Les samouraïs étaient tenus de dire la vérité et d'être 
                        honnêtes dans toutes leurs interactions. L'honnêteté et la sincérité étaient 
                        considérées comme des vertus essentielles d'un samouraï.
                    </p>
                    <p>
                        Loyauté (Chūgi) : 
                        La loyauté envers son seigneur féodal était sacrée pour un samouraï. 
                        Ils devaient être prêts à sacrifier leur vie pour leur maître et à servir avec 
                        dévouement et fidélité.
                    </p>
                    <p>
                        Contrôle de soi (Jisei) : 
                        Les samouraïs étaient tenus de maîtriser leurs émotions et leurs impulsions. 
                        Le contrôle de soi était considéré comme essentiel pour maintenir la dignité et 
                        la maîtrise de soi dans toutes les situations
                    </p>
                    
